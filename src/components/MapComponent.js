function adaptAirPoints(raw) {
  if (!Array.isArray(raw)) return [];
  return raw.map((p) => ({
    id: p.pointId,
    coords: [p.CoordinatesResponseDto.lon, p.CoordinatesResponseDto.lat], // [lon, lat]
    props: {
      hintContent: `${p.pointName} ‚Ä¢ AQI: ${p.europeanAqi}`,
      balloonContent: `
        <div style="font-size:13px;">
          <b>${p.pointName}</b><br/>
          PM2.5: ${p.pm25} ‚Ä¢ PM10: ${p.pm10}<br/>
          NO‚ÇÇ: ${p.nitrogenDioxide} ‚Ä¢ SO‚ÇÇ: ${p.sulphurDioxide}<br/>
          O‚ÇÉ: ${p.ozone} ‚Ä¢ CO‚ÇÇ: ${p.carbonDioxide}
        </div>
      `,
    },
  }));
}

